        Integer fkCarro = (Integer) cIdCarroDispositivo.getValue();;
        List<Dispositivo> dispositivos;
        Boolean hasModeloDispositivo = false;  
        Boolean hasCarroEmpresa = false;
        Integer fkEmpresa = 0;

